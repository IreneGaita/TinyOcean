package it.tinyOcean.model;

public class ContenutoBean {
	private String nomeArt;
	private float prezzoAcq;
	private int iva;
	private int quantita;
	
	public String getNomeArt() {
		return nomeArt;
	}
	public void setNomeArt(String nomeArt) {
		this.nomeArt = nomeArt;
	}
	public float getPrezzoAcq() {
		return prezzoAcq;
	}
	public void setPrezzoAcq(float prezzoAcq) {
		this.prezzoAcq = prezzoAcq;
	}
	public int getIva() {
		return iva;
	}
	public void setIva(int iva) {
		this.iva = iva;
	}
	public int getQuantita() {
		return quantita;
	}
	public void setQuantita(int quantita) {
		this.quantita = quantita;
	}

}
